/*

-------抽象类和接口的区别

    成员变量：
        抽象类：可以定义变量，也可以定义常量
        接口：只能定义常量

    成员方法：
        抽象类：可以定义具体方法，也可以定义抽象方法
        接口：只能定义抽象方法

    构造方法：
        抽象类：有
        接口：没有

---------------------------------------------------

    抽象类：对事物做抽象（描述事物）
    接口：对行为抽象（制定规则）

 */

